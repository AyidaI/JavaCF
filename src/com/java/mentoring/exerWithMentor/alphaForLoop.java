package com.java.mentoring.exerWithMentor;

public class alphaForLoop {
    public static void main(String[] args) {

        for(char ch='A' ; ch<='Z' ; ch++){

            if(ch=='Z'){
                System.out.println(ch);
            }else{
                System.out.print(ch+" - ");
            }
        }

    }
}
