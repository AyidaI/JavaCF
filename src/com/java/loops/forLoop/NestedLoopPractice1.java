package com.java.loops.forLoop;

public class NestedLoopPractice1 {
    public static void main(String[] args) {

        for( int i=0 ; i<5   ; i++  ){

            for( int k=0 ; k < 10   ; k++  ) {

                System.out.println("i>>" + i);
                System.out.println("K----" + k);
            }
            System.out.println("=======");
        }


    }
}
